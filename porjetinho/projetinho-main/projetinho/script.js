function redirectTo(type) {
    if (type === 'work') {
        window.location.href = 'work.html';
    } else if (type === 'gaming') {
        window.location.href = 'gaming.html';
    } else if (type === 'valor') {
        window.location.href = 'valor.html';
    } else if (type === 'notebook') {
        window.location.href = 'notebook.html';
    }
    
}
